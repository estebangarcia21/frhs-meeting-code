var money = 40;

// < - Less than
// > - Greater than
// <= - Less than or equal to
// >= - Greater than or equal to
// === - Equal to

if (money > 30) {
  console.log("Our money is greater than 30!");
} else if (money < 15) {
  console.log("Our money is less than 15!");
} else {
  console.log("Our money is in between 15 and 30");
}
