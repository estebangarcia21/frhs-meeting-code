// We are shopping at a grocery store.
// This application will calculate if we have enough money to buy whatever we have in the cart.

// Represents how much money we currently have in our wallet.
var money = 16;

// Total cost.
var totalCost = 18.30;

// If money is greater than total cost.
// Log: You have enough money!

if (money >= totalCost) {
  console.log('You have enough money!');
} else {
  console.log('You don\'t have enough money');
}
